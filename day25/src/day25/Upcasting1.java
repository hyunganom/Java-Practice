class Human{
	String name;
	int age;
	String hobby;
	public Human(String name, int age, String hobby) {//매개변수 생성자
		this.name = name;
		this.age = age;
		this.hobby = hobby;
		//매개변수 생성 꿀팁
		//우클릭-Source-Generate Constructor Using Fields
	}
	void info() {
		System.out.println("name:"+name);
		System.out.println("age:"+age);
		System.out.println("hobby:"+hobby);
	}
	
}
class Student extends Human{
	int grade;
	public Student(String name, int age, String hobby,int grade) {
		super(name, age, hobby);
		this.grade=grade;
	}
	void info(){//오버라이딩
		super.info();
		System.out.println("grade:"+grade);
	}
	void study() {
		System.out.println("공부하기");
	}
}
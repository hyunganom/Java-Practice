package day32;

public class Autoboxing_Unboxing {

	public static void main(String[] args) {
		//autoboxing
		Integer i= 10;
		Double d= 3.14;
		float f =3.14f;
		Character c ='A';
		
		//auto unboxing
		int ii=i;
		double dd=d;
		float ff=f;
		Character cc=c;
		System.out.println(ii);
		System.out.println(dd);
		System.out.println(ff);
		System.out.println(cc);
	}

}
